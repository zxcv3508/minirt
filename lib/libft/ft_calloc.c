#include "libft.h"

void		*ft_calloc(size_t c, size_t s)
{
	char	*re;
	size_t	i;

	i = 0;
	re = malloc(s * c);
	if (!re)
		return (0);
	while (i < s * c)
	{
		re[i] = 0;
		i++;
	}
	return ((void *)re);
}
